// Bhagwati Enterprises Admin — Service Worker
// Minimal SW required to make the app installable.
// We intentionally do NOT cache Firebase calls — stock data must always be fresh.

const CACHE_NAME = 'be-admin-shell-v1';
const SHELL_FILES = ['admin.html'];

self.addEventListener('install', (event) => {
  self.skipWaiting();
  event.waitUntil(
    caches.open(CACHE_NAME).then((cache) => cache.addAll(SHELL_FILES))
  );
});

self.addEventListener('activate', (event) => {
  event.waitUntil(
    caches.keys().then((keys) =>
      Promise.all(keys.filter((k) => k !== CACHE_NAME).map((k) => caches.delete(k)))
    )
  );
  self.clients.claim();
});

// Network-first: always try live data, fall back to cached shell only if offline
self.addEventListener('fetch', (event) => {
  if (event.request.method !== 'GET') return;
  event.respondWith(
    fetch(event.request).catch(() => caches.match(event.request))
  );
});
